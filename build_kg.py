    def get_validated_subgraph(self, drug_name, focus_gene, cancer_type):
        """
        Comprehensive fact-checker: Retrieves verified drug-target-disease paths,
        safety metadata, and direct source URLs.
        """
        query = """
        // --- FRAGMENT 1: Direct Treatment (Drug -> Disease) ---
        MATCH (d:Drug)-[r]-(dis:Disease)
        WHERE (toLower(d.name) CONTAINS toLower($drug_name) OR toLower($drug_name) CONTAINS toLower(d.name))
          AND r.layer = "VALIDATED"
          AND (toLower(dis.name) CONTAINS toLower($cancer_type) OR toLower($cancer_type) CONTAINS toLower(dis.name))
        RETURN d.name as subject, type(r) as interaction, dis.name as object, r.source as database, r.source_url as evidence_url, d.max_phase as drug_phase, d.is_withdrawn as withdrawn_flag

        UNION

        // --- FRAGMENT 2: Mechanism Step 1 (Drug -> Focus Gene) ---
        MATCH (d:Drug)-[r]-(g:Gene)
        WHERE (toLower(d.name) CONTAINS toLower($drug_name) OR toLower($drug_name) CONTAINS toLower(d.name))
          AND r.layer = "VALIDATED"
          AND (toLower(g.name) CONTAINS toLower($focus_gene) OR toLower($focus_gene) CONTAINS toLower(g.name))
        RETURN d.name as subject, type(r) as interaction, g.name as object, r.source as database, r.source_url as evidence_url, d.max_phase as drug_phase, d.is_withdrawn as withdrawn_flag

        UNION

        // --- FRAGMENT 3: Mechanism Step 2 (Focus Gene -> Disease) ---
        // We check this independently to confirm the gene's relevance to the disease
        MATCH (g:Gene)-[r]-(dis:Disease)
        WHERE (toLower(g.name) CONTAINS toLower($focus_gene) OR toLower($focus_gene) CONTAINS toLower(g.name))
          AND r.layer = "VALIDATED"
          AND (toLower(dis.name) CONTAINS toLower($cancer_type) OR toLower($cancer_type) CONTAINS toLower(dis.name))
        RETURN g.name as subject, type(r) as interaction, dis.name as object, r.source as database, r.source_url as evidence_url, null as drug_phase, null as withdrawn_flag

        UNION

        // --- FRAGMENT 4: Safety / Side Effects ---
        MATCH (d:Drug)-[r:CAUSES_SIDE_EFFECT]-(se:SideEffect)
        WHERE (toLower(d.name) CONTAINS toLower($drug_name) OR toLower($drug_name) CONTAINS toLower(d.name))
        RETURN d.name as subject, "CAUSES_SIDE_EFFECT" as interaction, se.name as object, r.source as database, r.source_url as evidence_url, d.max_phase as drug_phase, d.is_withdrawn as withdrawn_flag
        """
        with self.driver.session() as session:
            return session.run(query, drug_name=drug_name, focus_gene=focus_gene, cancer_type=cancer_type).data()

