    def load_synleth_smart_mapping(self, df):
        """
        Intelligently maps SynlethDB context strings to the best available
        node in the graph (Specific Disease > Broad Disease > Cell Line).
        """
        print(f"--- KG: Loading Synleth Data for {len(df)} rows ---")

        constraints = [
            # Gene Constraints
            "CREATE CONSTRAINT gene_name_unique IF NOT EXISTS FOR (g:Gene) REQUIRE g.name IS UNIQUE",

            # CellLine Constraints
            "CREATE CONSTRAINT cellline_id_unique IF NOT EXISTS FOR (c:CellLine) REQUIRE c.id IS UNIQUE",

            # Interaction Node Constraints (Crucial for the SL loader)
            "CREATE CONSTRAINT lethal_id_unique IF NOT EXISTS FOR (n:LethalInteraction) REQUIRE n.id IS UNIQUE",
            "CREATE CONSTRAINT nonlethal_id_unique IF NOT EXISTS FOR (n:NonLethalInteraction) REQUIRE n.id IS UNIQUE"
        ]

        query = """
        UNWIND $rows AS row

        // 1. Ensure Genes exist
        MERGE (g1:Gene {name: row['n1.name']})
        ON CREATE SET g1.entrez_id = row['n1.identifier'], g1.source = "SynlethDB"

        MERGE (g2:Gene {name: row['n2.name']})
        ON CREATE SET g2.entrez_id = row['n2.identifier'], g2.source = "SynlethDB"

        // 2. Sort names to create a unique ID for the pair to prevent duplicates
        WITH row, g1, g2,
            CASE WHEN row['n1.name'] < row['n2.name']
                  THEN row['n1.name'] + "_" + row['n2.name']
                  ELSE row['n2.name'] + "_" + row['n1.name']
            END AS pair_id

        // 3. Create the specific Interaction Node based on the 'is_lethal' flag
        // We use CALL apoc.do.when (or multiple FOREACH) to handle the two node types
        FOREACH (ignore IN CASE WHEN row.is_lethal = true THEN [1] ELSE [] END |
            MERGE (li:LethalInteraction {id: pair_id + "_lethal"})
            SET li.score = row['r.statistic_score'],
                li.pmid = row['r.pubmed_id'],
                li.source = row['r.source'],
                li.context_raw = row['r.cell_line'],
                li.last_updated = datetime()
            MERGE (g1)-[:PARTICIPATES_IN]->(li)
            MERGE (g2)-[:PARTICIPATES_IN]->(li)
        )

        FOREACH (ignore IN CASE WHEN row.is_lethal = false THEN [1] ELSE [] END |
            MERGE (ni:NonLethalInteraction {id: pair_id + "_nonlethal"})
            SET ni.score = row['r.statistic_score'],
                ni.pmid = row['r.pubmed_id'],
                ni.source = row['r.source'],
                ni.context_raw = row['r.cell_line'],
                ni.last_updated = datetime()
            MERGE (g1)-[:PARTICIPATES_IN]->(ni)
            MERGE (g2)-[:PARTICIPATES_IN]->(ni)
        )

        // 4. SMART MAPPING Logic (Links the Interaction Node to Disease/CellLine)
        WITH row, pair_id
        // Explicitly match the two possible interaction node IDs for this row
        MATCH (inter)
        WHERE inter.id IN [pair_id + "_lethal", pair_id + "_nonlethal"]

        // Define clean_hint AND original_raw for the following Tiers
        WITH inter, row,
             // Use coalesce to provide an empty string if the value is null/NaN
             split(toLower(coalesce(toString(row['r.cell_line']), "")), "_")[0] AS clean_hint,
             toLower(coalesce(toString(row['r.cell_line']), "")) AS original_raw

        // TIER 1: Match existing Clinical nodes
        OPTIONAL MATCH (d_spec:Disease)
        WHERE NOT d_spec:BroadDisease
          AND (toLower(d_spec.name) CONTAINS clean_hint OR clean_hint CONTAINS toLower(d_spec.name))

        // TIER 2: Match BroadDisease nodes
        OPTIONAL MATCH (d_broad:BroadDisease)
        WHERE toLower(d_broad.name) CONTAINS clean_hint OR clean_hint CONTAINS toLower(d_broad.name)

        // TIER 3: Match existing CellLines (Now original_raw is defined!)
        OPTIONAL MATCH (cl:CellLine)
        WHERE toLower(cl.name) = clean_hint
          OR toLower(cl.id) = original_raw
          OR toLower(cl.name) = original_raw

        // --- Draw Evidence Links from the INTERACTION NODE ---

        // 1. Link to Specific Disease if found
        FOREACH (x IN CASE WHEN d_spec IS NOT NULL THEN [1] ELSE [] END |
            MERGE (inter)-[r1:EVIDENCE_IN]->(d_spec)
            SET r1.source = "SynlethDB",
                r1.last_updated = datetime()
        )

        // 2. Link to Broad Disease ONLY IF no specific disease was found
        FOREACH (y IN CASE WHEN d_broad IS NOT NULL AND d_spec IS NULL THEN [1] ELSE [] END |
            MERGE (inter)-[r2:EVIDENCE_IN]->(d_broad)
            SET r2.source = "SynlethDB",
                r2.last_updated = datetime()
        )

        // 3. Link to Cell Line if found
        FOREACH (z IN CASE WHEN cl IS NOT NULL THEN [1] ELSE [] END |
            MERGE (inter)-[r3:EVIDENCE_IN]->(cl)
            SET r3.source = "SynlethDB",
                r3.last_updated = datetime()
        )
        """

        with self.driver.session() as session:
            for statement in constraints:
                session.run(statement)
            session.run(query, rows=df.to_dict('records'))
        print("✅ SynlethDB evidence successfully mapped to clinical and biological nodes.")

