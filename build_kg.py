    def load_chembl_data(self, df):
        """
        Loads ChEMBL data with synonym-aware merging, property appending,
        and static Phase node linking.
        """
        if df.empty: return

        def clean_synonyms(val):
            if isinstance(val, list):
                return val
            if isinstance(val, str) and val.startswith('['):
                try:
                    # Safely turns "['a', 'b']" into ['a', 'b']
                    return ast.literal_eval(val)
                except:
                    return []
            return []

        # Apply the fix directly to the dataframe before the session runs
        df['synonyms'] = df['synonyms'].apply(clean_synonyms)

        print(f"Loading {len(df)} ChEMBL Drug interactions with Synonym-matching...")

        query = """
        UNWIND $rows AS row

        // 1. STATIC PHASE NODE PREPARATION
        MERGE (p:Phase {name: "Phase " + toString(toInteger(row.max_phase))})

        // 2. TARGET GENE
        MERGE (g:Gene {name: row.gene})

        // --- ADD THIS WITH CLAUSE ---
        WITH row, p, g

        // 3. SMART DRUG MERGE (Synonym-Aware)
        OPTIONAL MATCH (d1:Drug {chembl_id: row.chembl_id})
        OPTIONAL MATCH (d2:Drug {name: row.drug})
        OPTIONAL MATCH (d3:Drug) WHERE d3.name IN row.synonyms

        WITH row, g, p, coalesce(d1, d2, d3) AS existing_drug

        // 4. MERGE OR CREATE THE DRUG
        MERGE (d:Drug {name: coalesce(existing_drug.name, row.drug)})
        ON CREATE SET d.source = [], d.source_url = []

        // 5. UPDATE PROPERTIES
        SET d.chembl_id = row.chembl_id,
            d.max_phase = row.max_phase,
            d.is_withdrawn = row.is_withdrawn,
            d.source = CASE WHEN NOT "ChEMBL" IN d.source THEN d.source + "ChEMBL" ELSE d.source END,
            d.source_url = CASE WHEN NOT row.source_url IN d.source_url THEN d.source_url + row.source_url ELSE d.source_url END

        // 6. RELATIONSHIPS
        MERGE (d)-[r1:REACHED_PHASE]->(p)
        SET r1.source = "ChEMBL", r1.source_url = row.source_url, r1.layer = "VALIDATED"

        MERGE (d)-[r2:TARGETS]->(g)
        SET r2.layer = "VALIDATED",
            r2.action_type = row.action,
            r2.source = "ChEMBL",
            r2.confidence = 0.9,
            r2.source_url = row.source_url

        FOREACH (syn_name IN row.synonyms |
            MERGE (s:Synonym {name: syn_name})
            MERGE (d)-[:HAS_SYNONYM]->(s)
        )
        """
        with self.driver.session() as session:
            session.run(query, rows=df.to_dict('records'))
        print("✅ ChEMBL Drug Properties and Synonyms loaded with consensus logic!")

    def load_drug_metadata_consensus(self, df):
        """
        Enriches Drug nodes with chemical metadata and targets.
        Handles multi-synonym splitting and property appending.
        """
        if df.empty: return
        print(f"Enriching {len(df)} Drug nodes with chemical metadata...")

        # We convert synonyms from a '|' string to a Python list for Cypher UNWIND
        df['synonym_list'] = df['synonyms'].fillna('').apply(lambda x: [s.strip() for s in x.split('|') if s.strip()])

        query = """
        UNWIND $rows AS row

        // 1. SMART MATCHING (Identity Consensus)
        // We search across all potential unique keys to find the existing node
        OPTIONAL MATCH (d_existing:Drug)
        WHERE d_existing.db_id = row.db_id
          OR d_existing.unii = row.UNII
          OR d_existing.cas = row.CAS
          OR toLower(d_existing.name) = toLower(row.drug_name)

        // 2. MERGE/CREATE THE DRUG NODE
        MERGE (d:Drug {name: coalesce(d_existing.name, row.drug_name)})

        // 3. UPDATE/APPEND PROPERTIES
        // We use COALESCE to keep existing data if the new row is missing it
        SET d.db_id = coalesce(row.db_id, d.db_id),
            d.cas = coalesce(row.CAS, d.cas),
            d.unii = coalesce(row.UNII, d.unii),
            d.smiles = coalesce(row.smiles, d.smiles),
            d.inchi = coalesce(row.inchi, d.inchi),
            // Append source without duplication
            d.source = CASE
                WHEN d.source IS NULL THEN ["DrugCentral"]
                WHEN NOT "DrugCentral" IN d.source THEN d.source + "DrugCentral"
                ELSE d.source
            END

        // 4. SYNONYM MANAGEMENT
        WITH d, row
        FOREACH (syn_name IN row.synonym_list |
            MERGE (s:Synonym {name: syn_name})
            MERGE (d)-[:HAS_SYNONYM]->(s)
        )

        // 5. TARGET RELATIONSHIP (Consensus Edge)
        WITH d, row
        MATCH (g:Gene {name: row.gene})
        MERGE (d)-[r:TARGETS]->(g)
        SET r.layer = "VALIDATED",
            r.action = row.action,
            r.source = CASE
                WHEN r.source IS NULL THEN ["DrugCentral"]
                WHEN NOT "DrugCentral" IN r.source THEN r.source + "DrugCentral"
                ELSE r.source
            END
        """
        with self.driver.session() as session:
            session.run(query, rows=df.to_dict('records'))
        print("✅ Drug nodes enriched with DrugBank/DrugCentral metadata.")

